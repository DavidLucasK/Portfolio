let cancelTypeWriter = false; // Vari√°vel para controlar a execu√ß√£o do typeWriter
let timeoutIds = []; // Armazena todos os timeouts para que possam ser cancelados

function typeWriter(elemento, textos, index = 0) {
  if (cancelTypeWriter) return; // Se cancelTypeWriter for true, interrompe a fun√ß√£o

  const originalFontSize = elemento.style.fontSize; // Armazena o tamanho original do texto

  const textoArray = textos[index].split("");
  elemento.innerHTML = "";

  textoArray.forEach((letra, i) => {
    const timeoutId = setTimeout(function () {
      if (!cancelTypeWriter) {
        // Verifica se a execu√ß√£o ainda deve continuar
        elemento.innerHTML += letra;
      }
    }, 100 * i);
    timeoutIds.push(timeoutId); // Armazena o ID do timeout
  });

  // Calcular o tempo total necess√°rio para escrever o texto
  const tempoTotal = 100 * textoArray.length;

  // Chamar a fun√ß√£o novamente ap√≥s o tempo necess√°rio para escrever o texto, mais um pequeno delay
  const nextTimeoutId = setTimeout(() => {
    if (!cancelTypeWriter) {
      // Verifica novamente antes de chamar a pr√≥xima itera√ß√£o
      const proximoIndex = (index + 1) % textos.length;
      typeWriter(elemento, textos, proximoIndex);
    } else {
      elemento.style.fontSize = originalFontSize; // Restaura o tamanho original quando a fun√ß√£o termina
    }
  }, tempoTotal + 1300); // 1300ms de delay antes de mudar para o pr√≥ximo texto
  timeoutIds.push(nextTimeoutId); // Armazena o ID do timeout

  // Restaura o tamanho original ap√≥s a √∫ltima letra
  setTimeout(() => {
    if (!cancelTypeWriter) {
      elemento.style.fontSize = originalFontSize;
    }
  }, tempoTotal);
}

